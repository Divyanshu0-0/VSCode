// Increment & Decrement Operators   #10

public class mark11 {
    public static void main(String []args) {
        int  i = 56 ;
        int b = i++;
        int c = ++i;
        System.out.println("Value of c: " + c);
        System.out.println(i++);
        System.out.println("Value of b: " + b);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
    }
}
